#ifndef _PWD_H_
#define _PWD_H_


/******************************************************************************
*                                                                             *
*   Include Files                                                             *
*                                                                             *
******************************************************************************/

#include <sys/types.h>                  // Include data types

/******************************************************************************
*                                                                             *
*   Global Defines, Variables and Macros                                      *
*                                                                             *
******************************************************************************/

struct passwd
{
    char *pw_name;                      // Login name
    uid_t pw_uid;                       // User ID
    gid_t pw_gid;                       // Group ID
    char *pw_dir;                       // User's home directory
    char *pw_shell;                     // Name of user's shell
};


/******************************************************************************
*                                                                             *
*   External Functions                                                        *
*                                                                             *
******************************************************************************/

extern struct passwd *getpwuid( uid_t uid );
extern struct passwd *getpwname( const char *name );


#endif //  _PWD_H_
